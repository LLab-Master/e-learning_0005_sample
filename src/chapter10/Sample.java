package chapter10;

public class Sample {
	private Integer id;
	private String name;

	public Sample(Integer id, String name) { // コンストラクタ
		super();
		this.id = id;
		this.name = name;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
